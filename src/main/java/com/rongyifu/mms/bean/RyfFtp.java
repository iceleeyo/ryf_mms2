package com.rongyifu.mms.bean;

public class RyfFtp {
	private String id;
	private String ftpIp;
	private String ftpName;
	private String ftpPsw;
	private int ftpPort;
	private String ftpPrivateKey;
	private String ftpPassphrase;
	private String remart;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFtpIp() {
		return ftpIp;
	}
	public void setFtpIp(String ftpIp) {
		this.ftpIp = ftpIp;
	}
	public String getFtpName() {
		return ftpName;
	}
	public void setFtpName(String ftpName) {
		this.ftpName = ftpName;
	}
	public String getFtpPsw() {
		return ftpPsw;
	}
	public void setFtpPsw(String ftpPsw) {
		this.ftpPsw = ftpPsw;
	}
	
	public int getFtpPort() {
		return ftpPort;
	}
	public void setFtpPort(int ftpPort) {
		this.ftpPort = ftpPort;
	}
	public String getFtpPrivateKey() {
		return ftpPrivateKey;
	}
	public void setFtpPrivateKey(String ftpPrivateKey) {
		this.ftpPrivateKey = ftpPrivateKey;
	}
	public String getFtpPassphrase() {
		return ftpPassphrase;
	}
	public void setFtpPassphrase(String ftpPassphrase) {
		this.ftpPassphrase = ftpPassphrase;
	}
	public String getRemart() {
		return remart;
	}
	public void setRemart(String remart) {
		this.remart = remart;
	}

}
